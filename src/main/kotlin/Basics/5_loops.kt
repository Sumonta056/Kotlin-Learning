package Basics

fun main()
{
    
    var i = 0
    while (i < 5)
    {
        println(i)
        i++
    }

    // * do - while loop
    // * oop will always be executed at least once,
    // * even if the condition is false
    var j = 0
    do {
        println(j)
        j++
    }
    while (j < 5)

}